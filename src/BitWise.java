public class BitWise {


    public static void main (String... args){
        int first_operand = 1;
        int second_operand = 4;

        System.out.println(first_operand & second_operand);
        System.out.println(first_operand ^ second_operand);
        System.out.println(first_operand | second_operand);

        System.out.println(0.0 / 0.0 == 0.0 / 0.0);

    }
}
